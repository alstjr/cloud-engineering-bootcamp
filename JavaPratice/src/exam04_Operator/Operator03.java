package exam04_Operator;
// 비교 연산자

public class Operator03 {

	public static void main(String[] args) {
		
		int n = 10;
		int n2 = 3;
		
		// 실행결과를 변수에 저장가능
		boolean result = n == n2;
		System.out.println(result);
		
		System.out.println(n == n2);
		System.out.println(n != n2);
		System.out.println(n > n2);
		System.out.println(n >= n2);
		System.out.println(n < n2);
		System.out.println(n <= n2);

	}

}
